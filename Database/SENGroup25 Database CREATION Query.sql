CREATE Table tblClient
(
ClientID VARCHAR(30) PRIMARY KEY NOT NULL,
ClientName VARCHAR(30) NOT NULL,
ClientSurname VARCHAR(30) NOT NULL,
ClientAddress VARCHAR(100) NOT NULL, 
Email VARCHAR(50) NOT NULL,
ContactNumber VARCHAR(12) NOT NULL
)
GO 
CREATE Table tblContract
(
ContractID VARCHAR(30) PRIMARY KEY NOT NULL,
ClientID VARCHAR(30) FOREIGN KEY REFERENCES tblClient(ClientID), 
ServiceLevel VARCHAR(20) NOT NULL,
ExperationDate VARCHAR(30) NOT NULL
)
GO 
CREATE Table tblEmployee
(
EmpID VARCHAR(30) PRIMARY KEY NOT NULL,
EmpName VARCHAR(30) NOT NULL,
EmpSurname VARCHAR(30) NOT NULL,
VatIDNumber VARCHAR(10) NOT NULL,
ContactNumber VARCHAR(12) NOT NULL,
Email VARCHAR(50) NOT NULL
);
GO 
CREATE Table tblAuthentication
(
AuthenticationID VARCHAR(30) PRIMARY KEY NOT NULL,
UserName VARCHAR(30)NOT NULL UNIQUE,
UserPassword VARCHAR(30)NOT NULL,
UserType VARCHAR(15)NOT NULL,
);
GO
CREATE Table tblJob
(
JobID VARCHAR(30) PRIMARY KEY NOT NULL,
ContractID VARCHAR(30) FOREIGN KEY REFERENCES tblContract(ContractID),
EmpID VARCHAR(30) FOREIGN KEY REFERENCES tblEmployee(EmpID),
ClientSatisfaction VARCHAR(5),
JobDescription VARCHAR(100) NOT NULL,
JobCategory VARCHAR(15) NOT NULL,
JobType VARCHAR(15) NOT NULL,
JobStatus VARCHAR(15) NOT NULL
)
GO
CREATE Table tblSkill
(
SkillID VARCHAR(30) PRIMARY KEY NOT NULL,
SkillDescription VARCHAR(80) NOT NULL,
SkillCategory VARCHAR(15) NOT NULL,
SkillType VARCHAR(15) NOT NULL
)
GO
CREATE Table tblEmployeeSkill
(
EmpID VARCHAR(30) FOREIGN KEY REFERENCES tblEmployee(EmpID) NOT NULL,
SkillID VARCHAR(30) FOREIGN KEY REFERENCES tblSkill(SkillID)  NOT NULL
)
GO
CREATE Table tblEquipment
(
EquipmentID VARCHAR(30) PRIMARY KEY NOT NULL,
EquipmentDescription VARCHAR(80) NOT NULL
)
GO 
CREATE Table tblJobEquipment
(
EquipmentID VARCHAR(30) FOREIGN KEY REFERENCES tblEquipment(EquipmentID) NOT NULL,
JobID VARCHAR(30) FOREIGN KEY REFERENCES tblJob(JobID) NOT NULL
)